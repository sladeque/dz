# Дано список рядків. З допомогою list comprehension створити новий список,
# який містить тільки рядки довші за 4 символи.
# Рядки в новому списку повинні бути у верхньому регістрі. Надрукувати новий список.
# Приклад:

list1 = ['apple', 'banana', 'cherry', 'date']
list2 = []
for i in list1:
    if len(i) > 4:
        list2.append(i.upper())
print(list2)